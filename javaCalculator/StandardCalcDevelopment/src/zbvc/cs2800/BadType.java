package zbvc.cs2800;

/**
 * Exception called when return value of get method called does not match that
 * of the object value.
 * 
 * @author zbvc854
 *
 */

@SuppressWarnings("serial")
public class BadType extends Exception {

  /**
   * Default constructor.
   */
  public BadType() {

  }

  /**
   * Constructor which takes a message parameter, which will be printed in the
   * case of an exception.
   * 
   * @param message
   *          Message to be printed to the screen.
   */
  public BadType(String message) {
    System.out.println(message);
  }

}
