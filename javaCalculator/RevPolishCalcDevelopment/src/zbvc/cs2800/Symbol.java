package zbvc.cs2800;

/**
 * Class to store symbols of type enumerations.
 * 
 * @author zbvc854
 *
 */

public enum Symbol {

  /**
   * A minus symbol.
   */
  MINUS,

  /**
   * A plus symbol.
   */
  PLUS,

  /**
   * A divide symbol.
   */
  DIVIDE,

  /**
   * A times symbol.
   */
  TIMES,

  /**
   * A left bracket symbol.
   */
  LEFTBRACKET,

  /**
   * A right bracket symbol.
   */
  RIGHTBRACKET,
  
  /**
   * Invalid symbol.
   */
  INVALID

}
